#include <stdio.h>

float soma(float n1, float n2)
{

    float resultado;

    resultado = n1 + n2;

    return resultado;
}

float subtracao(float n1, float n2)
{

    float resultado;

    resultado = n1 - n2;

    return resultado;
}

float divisao(float n1, float n2)
{

    float resultado;

    resultado = n1 / n2;

    return resultado;
}

float multiplicacao(float n1, float n2)
{

    float resultado;

    resultado = n1 * n2;

    return resultado;
}